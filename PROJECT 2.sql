CREATE DATABASE PROJECT_2;
USE PROJECT_2;

CREATE TABLE IF NOT EXISTS EMPLOYEE (
    EMP_NO INT,
    GENDER VARCHAR(20),
    MARITAL_STATUS VARCHAR(20),
    AGE_BAND VARCHAR(20),
    AGE INT, 
    DEPARTMENT VARCHAR(50),
    EDUCATION VARCHAR(50),
    EDUCATION_FIELD VARCHAR(50),
    JOB_ROLE VARCHAR(50),
    BUSINESS_TRAVEL VARCHAR(50),
    EMPLOYEE_COUNT INT,
    ATTRITION VARCHAR(20),
    ATTRITION_LABEL VARCHAR(50),
    JOB_SATISFACTION VARCHAR(20),
    ACTIVE_EMPLOYEE INT
);

SET SESSION SQL_MODE = '';

LOAD DATA INFILE 'C:\\Analyzing Employee Trends.csv'
INTO TABLE EMPLOYEE
FIELDS TERMINATED BY ',' 
OPTIONALLY ENCLOSED BY '"'
LINES TERMINATED BY '\n'
IGNORE 1 ROWS
(EMP_NO, GENDER, MARITAL_STATUS, AGE_BAND, AGE, DEPARTMENT, EDUCATION,
 EDUCATION_FIELD, JOB_ROLE, BUSINESS_TRAVEL, EMPLOYEE_COUNT, ATTRITION, 
 ATTRITION_LABEL, JOB_SATISFACTION, ACTIVE_EMPLOYEE);


SELECT * FROM EMPLOYEE;  


-- 1. Count the number of employees in each department---
SELECT DEPARTMENT, SUM(EMPLOYEE_COUNT) AS TOTAL_EMPLOYEE FROM EMPLOYEE
GROUP BY DEPARTMENT;

--- 2.Calculate the average age for each department ---
SELECT DEPARTMENT, AVG(AGE) AS AVERAGE_AGE FROM EMPLOYEE
GROUP BY DEPARTMENT;

--- 3. Identify the most common job roles in each department ---
SELECT DEPARTMENT, JOB_ROLE,ROLE_COUNT
FROM ( SELECT DEPARTMENT,JOB_ROLE, COUNT(*) AS ROLE_COUNT,
RANK() OVER (PARTITION BY DEPARTMENT ORDER BY COUNT(*) DESC) AS ROLE_RANK
FROM EMPLOYEE GROUP BY DEPARTMENT, JOB_ROLE
) AS ranked_roles
WHERE ROLE_RANK = 1;

--- 4. Calculate the average job satisfaction for each education level ---
SELECT EDUCATION, AVG(JOB_SATISFACTION) AS AVERAGE_JOB_SATISFACTION 
FROM EMPLOYEE GROUP BY EDUCATION;

--- 5.Determine the average age for employees with different levels of job satisfaction ---
SELECT JOB_SATISFACTION , AVG(AGE) AS AVERAGE_AGE FROM EMPLOYEE
GROUP BY JOB_SATISFACTION  ORDER BY JOB_SATISFACTION;
   
--- 6. Calculate the attrition rate for each age band --
SELECT AGE_BAND , COUNT(*) AS TOTAL_EMPLOYEES,
SUM(CASE WHEN ATTRITION = 'Yes' THEN 1 ELSE 0 END) AS ATTRITED_EMPLOYEES,
ROUND(
(SUM(CASE WHEN ATTRITION = 'Yes' THEN 1 ELSE 0 END) / COUNT(*)) * 100,2) AS ATTRITION_RATE_PERCENT
FROM EMPLOYEE
GROUP BY AGE_BAND
ORDER BY AGE_BAND;

-- 7. Identify the departments with the highest and lowest average job satisfaction ---
SELECT DEPARTMENT, ROUND(avg(JOB_SATISFACTION),2) AS AVG_JOB_SATISFACTION
FROM EMPLOYEE
GROUP BY DEPARTMENT
ORDER BY AVG_JOB_SATISFACTION DESC;

--- 8. Find the age band with the highest attrition rate among employees 
with a specific education level---

--- 9.Find the education level with the highest average job satisfaction 
among employees who travel frequently ---
SELECT EDUCATION, ROUND(AVG(JOB_SATISFACTION), 2) AS AVG_JOB_SATISFACTION
FROM EMPLOYEE
WHERE BUSINESS_TRAVEL = 'Travel_Frequently'
GROUP BY EDUCATION
ORDER BY AVG_JOB_SATISFACTION DESC
LIMIT 1;

--- 10. Identify the age band with the highest average job satisfaction among married employees -
SELECT AGE_BAND , ROUND(AVG(JOB_SATISFACTION),2) AS AVERAGE_JOB_SATISFACTION
FROM EMPLOYEE 
WHERE MARITAL_STATUS = 'MARRIED'
GROUP BY  AGE_BAND
ORDER BY AVERAGE_JOB_SATISFACTION DESC LIMIT 1;